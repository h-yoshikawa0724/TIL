public class Print {
    public static void main(String[] args) throws Exception {
        // 標準出力処理

        // System.out.println
        // 出力（改行あり）
        System.out.println("Hello Java!"); // Hello Java!

        // System.out.print
        // 出力（改行なし）
        // ※引数なしで改行のみの使い方もできる
        System.out.print("Hello World!"); // Hello World!
    }
}
